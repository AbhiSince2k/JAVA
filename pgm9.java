public static void main(String[] args) {
		char grade = 'A';
		
		switch (grade) {
		case 'A':
			System.out.println("Execelent");
			break;
		case 'B':
			System.out.println("Well Done");
			break;
		case 'C':
			System.out.println("Better");
			break;

		default:
			System.out.println("Invalid Grade!");
			break;
		}
		
	}
